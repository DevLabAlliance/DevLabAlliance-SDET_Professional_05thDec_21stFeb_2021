package com.devlabs.java.lab1;

public class LearnUnaryOperator {

	public static void main(String[] args) {
		int a = 5;
		System.out.println(a);
		System.out.println(a+1);
		System.out.println(a);
		a = a+1;
		a /= 10;
		System.out.println(a);
		System.out.println(++a);
		System.out.println(a++);
		System.out.println(a);
		
	}

}
